########################################################################
###########################Calculo del minimo###########################
########################################################################
$p=get-process
$l=$p.length
$min=$p[0].ID
for ($i=1;$i -lt $l; $i++)
{
	
	$id=$p[$i].ID
	if ($id -lt $min){
		$min=$id;
	}
}
echo "El ID mas alto es " $min
