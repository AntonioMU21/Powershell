########################################################################
###########################Calculo del maximo###########################
########################################################################
$p=get-process
$l=$p.length
$max=0;
for ($i=0;$i -lt $l; $i++)
{
	$id=$p[$i].ID
	if ($id -gt $max){
		$max=$id;
	}
}
echo "El ID mas alto es " $max
